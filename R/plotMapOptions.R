#' @export
plotMapOptions <- function(colArea = "#CCCCCC", sizeArea = 15, maxSizeArea = 15,
                           colLink = "#CCCCCC", sizeLink = 3, maxSizeLink = 10) {
  list(
    colArea = colArea,
    sizeArea = sizeArea, 
    maxSizeArea = maxSizeArea,
    colLink = colLink,
    sizeLink = sizeLink,
    maxSizeLink = maxSizeLink
  )
}